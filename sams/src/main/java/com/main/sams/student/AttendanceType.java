package com.main.sams.student;

/**
 * AttendanceType is an enum that represents the type of attendance.
 *
 * NONE - student's attendance is not set
 * PRESENT - student is present for the class
 * ABSENT - student is absent for the class
 * LATE - student is late for the class
 *
 * @author Cyprian
 * @version 1.0
 * @since 2023-01-04
 */
public enum AttendanceType {
    NONE, PRESENT, ABSENT, LATE
}
